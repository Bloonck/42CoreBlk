#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	char	*num;

	num = "0123456789";
	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -1 * nb;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
	}
	write(fd, &num[nb % 10], 1);
}
